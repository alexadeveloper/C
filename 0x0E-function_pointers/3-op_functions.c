#include "3-calc.h"
#include <stdio.h>

/**
  *op_add - option sum
  *@a: is a int
  *@b: is a int
  *Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  *op_sub - option difference
  *@a: is a int
  *@b: is a int
  *Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
  *op_mul - option product
  *@a: is a int
  *@a: is a int
  *@b: is a int
  *Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  *op_div - option division
  *@a: is a int
  *@b: is a int
  *@b: is a int
  *Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  *op_mod - option modular
  *@a: is a int
  *@b: is a int
  *Return: a + b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
